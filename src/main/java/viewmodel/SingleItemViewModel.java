package viewmodel;

import java.io.Serializable;

public class SingleItemViewModel implements Serializable {

    private int productId;
    private String productName;
    private int productPrice;
    private String productImage;
    private String productDescription;
    private String productAllergy;

    // + option の情報
    private int optionId;
    private String optionName;
    private int optionPrice;

    public SingleItemViewModel() {}

	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public int getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

	public String getProductImage() {
		return productImage;
	}

	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}

	public String getProductDescription() {
		return productDescription;
	}

	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}

	public String getProductAllergy() {
		return productAllergy;
	}

	public void setProductAllergy(String productAllergy) {
		this.productAllergy = productAllergy;
	}

	public int getOptionId() {
		return optionId;
	}

	public void setOptionId(int optionId) {
		this.optionId = optionId;
	}

	public String getOptionName() {
		return optionName;
	}

	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}

	public int getOptionPrice() {
		return optionPrice;
	}

	public void setOptionPrice(int optionPrice) {
		this.optionPrice = optionPrice;
	}
    
}
